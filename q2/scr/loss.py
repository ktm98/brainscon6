class CustomBCEWithLogitsLoss(nn.Module):
    def __init__(self, weight=[1, 1]):
        super().__init__()
        self.weight = weight
    def forward(self, inputs, targets):
        # inputはmaskとlabelのtuple, targetはmask_predとlabel_predのtuple
        assert type(inputs) == tuple
        bce0 = F.binary_cross_entropy_with_logits(inputs[0], targets[0])
        bce1 = F.binary_cross_entropy_with_logits(inputs[1], targets[1])
        loss = bce0*self.weight[0] + bce1*self.weight[1]
        return loss



